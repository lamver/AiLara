    function setMainTheme(theme = 'light') {

        if (theme === 'auto') {
            let userTheme;

            if ((userTheme = localStorage.getItem("main-theme-set-user"))) {
                if (userTheme == 'dark') {
                    setDarkTheme();
                    return;
                }

                setLightTheme();

                return;
            }

            if (window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches) {
                setDarkTheme();

                return;
            }

            setLightTheme()

            return;
        }

        if (theme === 'light') {
            setLightTheme()

            return;
        }

        if (theme === 'dark') {
            setDarkTheme()

            return;
        }
    }

